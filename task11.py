weeknumber=int(input("Enter week number:"))

if weeknumber==1:
    print("sunday")

elif weeknumber==2:
    print("monday")

elif weeknumber==3:
    print("tuesday")

elif weeknumber==4:
    print("wednesday")

elif weeknumber==5:
    print("thursday")

elif weeknumber==6:
    print("friday")

elif weeknumber==7:
    print("saturday")

else:
    print("not")